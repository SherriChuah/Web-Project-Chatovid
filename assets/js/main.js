var url = "https://api.coronavirus.data.gov.uk/v1/data?%27%20+%20%27filters=areaType=utla;areaName=manchester&structure={%22date%22:%22date%22,%22areaName%22:%22areaName%22,%22dailyC%22:%22newCasesByPublishDate%22,%22cumC%22:%22cumCasesByPublishDate%22,%22death%22:%22newDeaths28DaysByPublishDate%22,%22deathC%22:%22cumDeaths28DaysByPublishDate%22}"

async function getStat(url, thing, id){
  const response = await fetch(url);
  var data = await response.json();
  var output = await data["data"][0][thing];
  document.getElementById(id).setAttribute("data-purecounter-end", output);
  return output;
}

function successCallback(result){
  console.log(result);    
}

function failureCallback(error){
  console.error(error);
}

getStat(url, "dailyC", "daily").then(successCallback, failureCallback);
getStat(url, "cumC", "cumm").then(successCallback, failureCallback);
getStat(url, "death", "death").then(successCallback, failureCallback);
getStat(url, "deathC", "deathC").then(successCallback, failureCallback);

